function getLang() {
  const saved = localStorage.getItem("lang");
  return saved === "en" ? "en" : "pt";
}

function setLang(lang) {
  localStorage.setItem("lang", lang);
}

function applyLang(lang) {
  document.documentElement.lang = lang;
  const dict = I18N[lang];

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = dict[key];
    if (typeof val === "string") el.innerHTML = val;
  });

  const metaTitleKey = document.body?.getAttribute("data-meta-title");
  const metaDescKey = document.body?.getAttribute("data-meta-desc");

  if (metaTitleKey && dict[metaTitleKey]) document.title = dict[metaTitleKey];

  if (metaDescKey && dict[metaDescKey]) {
    let tag = document.querySelector('meta[name="description"]');
    if (!tag) {
      tag = document.createElement("meta");
      tag.setAttribute("name", "description");
      document.head.appendChild(tag);
    }
    tag.setAttribute("content", dict[metaDescKey]);
  }
}

window.VNV_I18N = { getLang, setLang, applyLang };

// initial apply (in case app.js loads after)
applyLang(getLang());
